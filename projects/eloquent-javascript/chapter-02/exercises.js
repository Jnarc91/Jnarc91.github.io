function drawChessboard(num) {
  var board = [];
  //for loop to iterate through array lines
  for (var i = 0; i < num; i++) {
    for (let j = 0; j < num; j++) {
    //nested loop to iterate through array lines
    if((i + j) % 2 === 0) {
      //sapce for even indexes
      board += " ";
    } else {
      board += "#"// used for odd indexes
     }
    }
    board += "\n";
  } console.log(board);
}





