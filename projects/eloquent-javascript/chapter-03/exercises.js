// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
//I: number
//O: boolean
//C: number must be positive
if (number < 0) {
  return false;
  } else if (number % 2 === 0) {
    return true; 
  } else {
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var numChars = 0;
  for (var i = 0; i <= string.length - 1; i ++) {
    if (string[i] === char) {
      numChars++;
    }
  }
  return numChars;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
var bCount = 0;
for (var i = 0; i <= string.length - 1; i++) {
  if(string[i] === "B") {
    bCount++;
  }
}
return bCount;
}

