// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(arg1, arg2) {
if (arg1 < arg2) {
return arg1;
} else if (arg2 < arg1) {
  return arg2;
} else if (arg2 === arg1) {
  return 0;
  }
}
